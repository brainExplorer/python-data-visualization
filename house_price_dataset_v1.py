def clean_boolean(val):
  """It standardizes boolean columns like Yes/No."""
  if pd.isna(val):
    return np.nan
  val = str(val).strip().lower()
  if val == '':
    return np.nan
  if val in ['evet', 'true', '1', 'yes']:
    return 1
  elif val in ['hayır', 'false', '0', 'no']:
    return 0
  else:
    return np.nan

